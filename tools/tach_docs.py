def main() -> int:
    """Generate architecture documentation."""
    print("Generating architecture documentation...")

    # Ensure output directory exists
    output_dir = (
        Path(__file__).parent.parent / "docs" / "architecture" / "dependencies"
    )
    output_dir.mkdir(parents=True, exist_ok=True)

    # Generate dependency graph
    print("  - Generating dependency graph...")
    mermaid = get_mermaid_diagram()
    if not mermaid.strip():
        print("    WARNING: tach show --mermaid returned empty output")
        print("    Make sure tach is installed and tach.toml is configured")
        return 1

    graph_html = generate_graph_html(mermaid)
    graph_path = output_dir / "dependency_graph.html"
    graph_path.write_text(graph_html, encoding="utf-8")
    print(f"    Created: {graph_path}")

    print("\nDone! Open docs/architecture/dependencies/dependency_graph.html to view.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
